    <script>
        // Hàm để tạo dropdown cho ngày, tháng và năm
        function populateDateSelects() {
            const daySelect = document.getElementById('day');
            const monthSelect = document.getElementById('month');
            const yearSelect = document.getElementById('year');

            // Tạo dropdown cho ngày
            for (let i = 1; i <= 31; i++) {
                const option = document.createElement('option');
                option.value = i;
                option.textContent = i;
                daySelect.appendChild(option);
            }

            // Tạo dropdown cho tháng
            for (let i = 1; i <= 12; i++) {
                const option = document.createElement('option');
                option.value = i;
                option.textContent = i;
                monthSelect.appendChild(option);
            }

            // Tạo dropdown cho năm (ví dụ: từ 1900 đến 2100)
            const currentYear = new Date().getFullYear();
            for (let i = currentYear - 100; i <= currentYear; i++) {
                const option = document.createElement('option');
                option.value = i;
                option.textContent = i;
                yearSelect.appendChild(option);
            }
        }

        // Gọi hàm để tạo các dropdown
        populateDateSelects();

        // Hàm hiển thị giá trị đã chọn
        function displaySelectedDate() {
            const day = document.getElementById('day').value;
            const month = document.getElementById('month').value;
            const year = document.getElementById('year').value;

            const dateDisplay = document.getElementById('dateDisplay');
            dateDisplay.textContent = `Bạn đã chọn: Ngày ${day}, Tháng ${month}, Năm ${year}`;
        }

        // Thêm sự kiện thay đổi cho mỗi dropdown
        document.getElementById('day').addEventListener('change', displaySelectedDate);
        document.getElementById('month').addEventListener('change', displaySelectedDate);
        document.getElementById('year').addEventListener('change', displaySelectedDate);
    </script>
